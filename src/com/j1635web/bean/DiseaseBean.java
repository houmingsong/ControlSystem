package com.j1635web.bean;
/**
 * 病害实体bean
 * @author 乔荣升
 *
 */
public class DiseaseBean {
	/**
	 * 病害id
	 */
	private Integer id;
	/**
	 * 病害名称
	 */
	private String name;
	/**
	 * 病源
	 */
	private String pathogen;
	/**
	 * 发病症状
	 */
	private String symptom;
	/**
	 * 发病规律
	 */
	private String  law;
	/**
	 * 图片
	 */
	private String image;
	/**
	 * 防治措施
	 */
	private String defend;
	/**
	 * 主要危害
	 */
	private String harm;
	public DiseaseBean(Integer id, String name, String pathogen, String symptom, String law, String image,
			String defend, String harm) {
		super();
		this.id = id;
		this.name = name;
		this.pathogen = pathogen;
		this.symptom = symptom;
		this.law = law;
		this.image = image;
		this.defend = defend;
		this.harm = harm;
	}
	public DiseaseBean() {
		super();
		// TODO Auto-generated constructor stub
	}
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getPathogen() {
		return pathogen;
	}
	public void setPathogen(String pathogen) {
		this.pathogen = pathogen;
	}
	public String getSymptom() {
		return symptom;
	}
	public void setSymptom(String symptom) {
		this.symptom = symptom;
	}
	public String getLaw() {
		return law;
	}
	public void setLaw(String law) {
		this.law = law;
	}
	public String getImage() {
		return image;
	}
	public void setImage(String image) {
		this.image = image;
	}
	public String getDefend() {
		return defend;
	}
	public void setDefend(String defend) {
		this.defend = defend;
	}
	public String getHarm() {
		return harm;
	}
	public void setHarm(String harm) {
		this.harm = harm;
	}
	@Override
	public String toString() {
		return "DiseaseBean [id=" + id + ", name=" + name + ", pathogen=" + pathogen + ", symptom=" + symptom + ", law="
				+ law + ", image=" + image + ", defend=" + defend + ", harm=" + harm + "]";
	}
	
}
